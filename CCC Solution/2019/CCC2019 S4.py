import math
n,k = map(int,input().split())
a = list(map(int,input().split()))
ans = 0
rem = n%k
minday = math.ceil(n/k)


memo = {}

def maxnum(s,e,c=-1): # start, end
    x = memo.get(str([s,e,c]),False)
    if x: return x
    else:
        if c==-1: memo[str([s,e,c])] = max(a[s:e])
        else: memo[str([s,e,c])] = max(a[:c][s:e])
        return memo[str([s,e,c])]
         

ans = 0
if rem:
    for i in range(n):
        if i//k+2+(n-i-k)//k>minday: continue
        cur = 0
        for j in range(0,i-1,k):
            cur+=maxnum(j,j+k,i)
        cur+=max(a[i:i+k])
        for j in range(i+k,n,k):
            cur+=maxnum(j,j+k)
            max(a[j:j+k])
        ans = max(ans,cur)
    print(ans)
else: 
    print(sum([max(a[j:j+k]) for j in range(0, n, k)]))
