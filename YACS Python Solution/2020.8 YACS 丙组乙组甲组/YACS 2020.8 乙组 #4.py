def stdinput():
    '''simple input function'''
    def strint(x):
        try: return int(x)
        except: return x
    return list(map(strint,input().strip().split()))
def solution():
    n,a,b,c,d = stdinput()
    dp = [[0]*(4) for _ in range(n+1)]
    dp[a][0] = 1
    for i in range(a+1,n+1):
        dp[i][0] = max(dp[i-a][0],dp[i-d][3])+1 #
        dp[i][1] = max(dp[i-a][0],dp[i-d][3])
        dp[i][2] = dp[i-c][1]
        dp[i][3] = max(dp[i-a][0],dp[i-c][2]*2)
    print(max(dp[n]))

for i in range(5):
    solution()